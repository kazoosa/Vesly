/**
 * Lucide-style inline icons. Kept as inline SVGs so we don't need to ship
 * the full lucide-react dependency for ~10 icons.
 *
 * Stroke weight is 2px (Lucide default). Size defaults to 16 for inline use,
 * 20 for nav. All icons use `currentColor`.
 */

interface Props {
  size?: number;
  strokeWidth?: number;
  className?: string;
}

function Wrap({
  children,
  size = 16,
  strokeWidth = 2,
  className,
}: Props & { children: React.ReactNode }) {
  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
      aria-hidden="true"
    >
      {children}
    </svg>
  );
}

